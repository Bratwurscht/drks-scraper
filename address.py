def Address(object):
	def __init__(self, institution, name, street, postcode, city, country, phone, fax, mail, url):
		self.Institution = institution
		self.Name = name
		self.Street = street
		self.Postcode = postcode
		self.City = city
		self.Country = country
		self.Phone = phone
		self.Fax = fax
		self.Mail = mail
		self.Url = url
